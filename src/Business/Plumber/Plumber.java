package Business.Plumber;


/**
 *
 * @author Dinesh
 */
public class Plumber {

    private String plumberNo;
    private String plumberName;
    private String plumberPhone;
    private String plumberEmail;

    public String getPlumberNo() {
        return plumberNo;
    }

    public void setPlumberNo(String plumberNo) {
        this.plumberNo = plumberNo;
    }

    public String getPlumberName() {
        return plumberName;
    }

    public void setPlumberName(String plumberName) {
        this.plumberName = plumberName;
    }

    public String getPlumberPhone() {
        return plumberPhone;
    }

    public void setPlumberPhone(String plumberPhone) {
        this.plumberPhone = plumberPhone;
    }

    public String getPlumberEmail() {
        return plumberEmail;
    }

    public void setPlumberEmail(String plumberEmail) {
        this.plumberEmail = plumberEmail;
    }
        
}
