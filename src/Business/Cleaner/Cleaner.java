package Business.Cleaner;



/**
 *
 * @author Dinesh
 */
public class Cleaner {

    private String cleanerNo;
    private String cleanerName;
    private String cleanerPhone;
    private String cleanerEmail;

    public String getCleanerNo() {
        return cleanerNo;
    }

    public void setCleanerNo(String cleanerNo) {
        this.cleanerNo = cleanerNo;
    }

    public String getCleanerName() {
        return cleanerName;
    }

    public void setCleanerName(String cleanerName) {
        this.cleanerName = cleanerName;
    }

    public String getCleanerPhone() {
        return cleanerPhone;
    }

    public void setCleanerPhone(String cleanerPhone) {
        this.cleanerPhone = cleanerPhone;
    }

    public String getCleanerEmail() {
        return cleanerEmail;
    }

    public void setCleanerEmail(String cleanerEmail) {
        this.cleanerEmail = cleanerEmail;
    }
    
}
